export default function setGlobalColorTheme(
    themeMode: "light" | "dark",
    color: ThemeColors,
) {
    const validColor = color in themes ? color : "Telegram"
    const theme = themes[validColor][themeMode] as { [key: string]: string }

    for (const key in theme) {
        document.documentElement.style.setProperty(`--${key}`, theme[key])
        if (key === "primary") {
            window.dispatchEvent(new Event("primary-color-change"))
        }
    }
}
